def solution(n):
    i = 1
    # n과 1의 갯수가 같은 값 중 다음으로 큰값이 생각보다 크지 않으므로 반복문을 활용하여 찾을 수 있음
    while bin(n).count('1') != bin(n+i).count('1'):
        i += 1
    answer = n + i
    return answer

